class MathUtils:
    @staticmethod
    def summation(value_1, value_2):
        return value_1 + value_2


    @staticmethod
    def difference(value_1, value_2):
        return value_1 - value_2


    @staticmethod
    def multiplication(value_1, value_2):
        return value_1 * value_2


    @staticmethod
    def division(value_1, value_2):
        return value_1 / value_2
