class MathUtils:
    def __init__(self, value_1, value_2):
        self.value_1 = value_1
        self.value_2 = value_2


    def summation(self):
        return self.value_1 + self.value_2


    def difference(self):
        return self.value_1 - self.value_2


    def multiplication(self):
        return self.value_1 * self.value_2


    def division(self):
        return self.value_1 / self.value_2
